def getArea(r, pi=3.14159):
    """Doyraning uzunligini qaytaruvchi funksiya"""
    return pi*(r**2)

def getPerimeter(r,pi=3.14159):
    """Doyraning piremeterini qaytaruvchi funksiya"""
    return 2*pi*r